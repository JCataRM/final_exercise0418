package Data;

public class OperationsCalculator {
    public float num1;
    public float num2;

    public float addOperation() {
        return this.num1 + this.num2;
    }

    public float substractOperation() {
        return this.num1 - this.num2;
    }

    public float multiplyOperation() {
        return this.num1 * this.num2;
    }

    public float divideOperation() {
        return this.num1 / this.num2;
    }
}
